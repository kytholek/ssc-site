foreach ($dir in (Get-ChildItem "c:\App Projects\Website\ssc-site\ssc-site2\ssc-site\blog\" -Directory)) {
    $indexFile = Join-Path $dir.FullName "index.html"
    if (!(Test-Path $indexFile)) { continue }
    
    $dirName = $dir.Name
    if (!$relatedPostsMap.ContainsKey($dirName)) { continue }
    
    $content = Get-Content $indexFile -Raw -Encoding UTF8
    
    # Skip if already has related-posts section
    if ($content -like "*related-posts*") {
        Write-Host "Skip: $dirName (already added)"
        continue
    }
    
    # Build related posts section
    $html = "  " + '<div class="related-posts">' + [System.Environment]::NewLine
    $html += "    " + '<div class="related-posts-title">Related Articles</div>' + [System.Environment]::NewLine
    $html += "    " + '<div class="related-posts-grid">' + [System.Environment]::NewLine
    
    foreach ($relatedDir in $relatedPostsMap[$dirName]) {
        $relatedTitle = $titleMap[$relatedDir]
        $url = "/blog/" + $relatedDir + "/"
        $html += "      " + '<a href="' + $url + '" class="related-post-card">' + [System.Environment]::NewLine
        $html += "        " + '<div class="related-post-title">' + $relatedTitle + '</div>' + [System.Environment]::NewLine
        $html += "        " + '<span class="related-post-link">Read More →</span>' + [System.Environment]::NewLine
        $html += "      " + '</a>' + [System.Environment]::NewLine
    }
    
    $html += "    " + '</div>' + [System.Environment]::NewLine + "  " + '</div>'
    
    # Insert before footer
    $newContent = $content -replace '(  </div>\s*)(</footer>)', ('  </div>' + [System.Environment]::NewLine + [System.Environment]::NewLine + $html + [System.Environment]::NewLine + [System.Environment]::NewLine + '$2')
    
    if ($newContent -ne $content) {
        Set-Content -Path $indexFile -Value $newContent -Encoding UTF8 -Force
        Write-Host "Added: $dirName"
    } else {
        Write-Host "Failed: $dirName"
    }
}

